        public static string DumpException(Exception exception)
        {
            string result = "";
            if (exception != null)
            {
                result = "Message: " + exception.Message + "\r\nStack Trace: " + exception.StackTrace + "\r\nSource: " + exception.Source;
                if (exception.InnerException != null)
                    result += "\r\n***Inner Exception" + DumpException(exception.InnerException);
            }

            return result;
        }

		public static string NormalizeLink(this string url, string baseUri)
		{
			if (string.IsNullOrEmpty(url))
			{
				return baseUri;
			}
			if (Uri.IsWellFormedUriString(url, UriKind.Relative))
			{
				if (!string.IsNullOrEmpty(baseUri))
				{
					Uri absoluteBaseUrl = new Uri(baseUri, UriKind.Absolute);
					return new Uri(absoluteBaseUrl, url).ToString();
				}
				return new Uri(url, UriKind.Relative).ToString();
			}
			if (Uri.IsWellFormedUriString(url, UriKind.Absolute))
			{
				return new Uri(url, UriKind.Absolute).ToString();
			}
			return null;
		}

