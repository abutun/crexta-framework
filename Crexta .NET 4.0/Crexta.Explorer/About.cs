using System;
using System.Linq;
using System.Windows.Forms;

namespace Crexta.Explorer
{
    public partial class About : Form
    {
        public About()
        {
            InitializeComponent();
        }

        private void linkLabel1_LinkClicked(object sender, LinkLabelLinkClickedEventArgs e)
        {
            this.linkLabel1.LinkVisited = true;
            System.Diagnostics.Process.Start("http://www.crexta.com?ref=crexta.Explorer");
        }

        private void linkLabel2_LinkClicked(object sender, LinkLabelLinkClickedEventArgs e)
        {
            this.linkLabel2.LinkVisited = true;
            System.Diagnostics.Process.Start("http://www.turkcrawler.com?ref=crexta.Explorer");
        }

        private void About_Load(object sender, EventArgs e)
        {
            this.versionLabel.Text = "Version " + Utilities.Version.ExplorerVersion;
        }
    }
}
